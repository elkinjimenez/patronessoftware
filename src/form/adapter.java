    private void generarAbstractActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarAbstractActionPerformed
        // TODO add your handling code here:

        DecimalFormat formateador = new DecimalFormat("###,###.##");
        Adaptador conversor = new Adaptador();
        int opci = JOptionPane.YES_OPTION;
        
        Double pesos;
        Double pesosimprimir = 0.0;
        
        pesos = Double.parseDouble(this.pesos.getText());
        pesosimprimir = pesos;
        conversor.ingresarPesos(pesos);
        
        opci = JOptionPane.showConfirmDialog(null, "Desea agregar m치s pesos?", "agregar",
                JOptionPane.YES_NO_OPTION);
        
        try {
            
            while (opci == JOptionPane.YES_OPTION) {
                pesos = Double.parseDouble(JOptionPane.showInputDialog("Ingrese los pesos adicionales"));
                pesosimprimir += pesos;
                
                conversor.ingresarPesos(pesos);
                opci = JOptionPane.showConfirmDialog(null, "Desea agregar m치s pesos?", "agregar",
                        JOptionPane.YES_NO_OPTION);
            }
            
        } catch (Exception e) {
            
        }
        
        resultado.setText("Total de d칩lares: " + formateador.format(conversor.getSaldo()) + "\nPesos ingresados: " + formateador.format(pesosimprimir));
    }//GEN-LAST:event_generarAbstractActionPerformed

